      CHARACTER*1 TARGET
      DOUBLE PRECISION PARAM(23),COV(23,23)
      DOUBLE PRECISION F2P,F2R
      DOUBLE PRECISION XP,XR,CP,CR,AP,AR,BP,BR,t,dQ2,dx,dW2
      double precision alpha_em,aprmass,pi,hbarc2,gam2
